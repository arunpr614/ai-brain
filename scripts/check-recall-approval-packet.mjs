function checkDoc(doc, findings) {
  const summary = {
    id: doc.id,
    path: doc.path,
    exists: existsSync(doc.path),
    requiredSnippets: doc.required.length,
    missingSnippets: 0,
  };

  if (!summary.exists) {
    findings.push({ doc: doc.id, path: doc.path, message: "Required Recall approval packet document is missing." });
    summary.missingSnippets = doc.required.length;
    return summary;
  }

  const content = readFileSync(doc.path, "utf8");
  for (const snippet of doc.required) {
    if (!content.includes(snippet)) {
      summary.missingSnippets += 1;
      findings.push({ doc: doc.id, path: doc.path, message: `Missing required snippet: ${snippet}` });
    }
  }

  const leakedKey = content.match(/\bsk_[A-Za-z0-9_-]{10,}\b/);
  if (leakedKey) {
    findings.push({
      doc: doc.id,
      path: doc.path,
      message: "Document appears to contain a Recall API key-shaped value.",
    });
  }

  return summary;
}

function checkPackageScripts(scripts, findings) {
  const packageJsonPath = `${ROOT}/package.json`;
  const summary = {
    path: "package.json",
    exists: existsSync(packageJsonPath),
    requiredScripts: scripts.length,
    missingScripts: 0,
  };

  if (!summary.exists) {
    findings.push({ path: "package.json", message: "package.json is missing." });
    summary.missingScripts = scripts.length;
    return summary;
  }

  const packageJson = JSON.parse(readFileSync(packageJsonPath, "utf8"));
  for (const scriptName of scripts) {
    if (!packageJson.scripts?.[scriptName]) {
      summary.missingScripts += 1;
      findings.push({ path: "package.json", message: `Missing required script: ${scriptName}` });
    }
  }

  return summary;
}

function parseArgs(argv) {
  const parsed = { help: false };
  for (const arg of argv) {
    if (arg === "--help") {
      parsed.help = true;
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }
  return parsed;
}

